#include <stdio.h>
int main()
{
	printf("Saya belajar pemrograman dengan bahasa ( %c )  >> char \n", 'C');    // char   
	printf("Saya baru saja menyelesaikan Bab ( %d ) >> integer \n", 2);          // double
	printf("Saya sudah siap ( %.1f ) persen untuk move on ", 99.9);     // float
	printf("ke Bab selanjutnya! >> float \n");
	return 0;
}
