#include <stdio.h>

int main() {
	int counter = 0;
	int counter2;
	printf("Counter = %d, Counter2= %d\n", counter, counter2);
	
	// resetting a variable
	counter = 4;
	counter2 = 5;
	printf("Counter = %d, Counter2= %d\n", counter, counter2);
	
	return 0;
}
